package ejemplos;

public class Ej34RotuloConFunciones {

	public static void letraM() {
		System.out.println("*   *");
		System.out.println("* * *");
		System.out.println("* * *");
		System.out.println("*   *");
		System.out.println("*   *");
		System.out.println("\n\n");
	}
	
	public static void letraA() {
		System.out.println("* * *");
		System.out.println("*   *");
		System.out.println("* * *");
		System.out.println("*   *");
		System.out.println("*   *");
		System.out.println("\n\n");	
	}
	public static void main(String[] args) {
		letraM();
		letraA();
		letraM();
		letraA();
	}
	
	
	
}
